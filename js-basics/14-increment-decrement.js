// These are ++ or -- and used to increase or decrease a nunmber by 1

let n1 = 10, n2 = 10, n3 = 10, n4 = 10;

console.log(n1++); // 10 -> variable++ is known as post-increment 
console.log(++n2); // 11 -> ++variable is know as pre-increment 

console.log(n1); // 11
console.log(n2); // 11


console.log(n3--); // 10
console.log(--n4); // 9

console.log(n3); // 9
console.log(n4); // 9


